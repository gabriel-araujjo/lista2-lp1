#ifndef QUESTAO_1_H_
#define QUESTAO_1_H_

/**
 * Calcula a soma dos n primeiros elementos de uma sequencia harmônica
 *
 * @param n - elemento final da soma harmonica
 * @return soma dos n primeiros elementos de uma sequencia harmônica
 *
 * https://en.wikipedia.org/wiki/Harmonic_series_(mathematics)
 * Implementação recursiva
 */
double soma_sequencia_harmonica_r(int n);

/**
 * Calcula o somatório da série
 * ∑(i^2 + 1)/(i + 3) com i variando de 1 até n
 *
 * @param n - elemento final da soma
 * @return soma dos n primeiros elementos de uma sequencia
 *
 * Implementação recursiva
 */
double soma_sequencia_b_r(int n);

#endif // QUESTAO_1_H_
